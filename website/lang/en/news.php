<h1>Latest News</h1>
<p>The new version 0.1.7 is fresh out.</p>
<p>This release is the last one before the upcoming 0.2.0,
which is developed currently. It includes three new fields for
post office box, country and title and a new uninstallation
command for the setup script.</p>

<p>A lot of bugs and issues were fixed too. The most important
fixes concern encoding of terminal output and phone book files,
which are now handled correctly. Moreover message files are
search in the correct path, which means, that you get
internationalized output even if tel is not installed in /usr/.
Output messages and formatting were also slightly improved.</p>
