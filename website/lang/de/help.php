<h1>Unterstütze tel</h1>

<p>Falls du daran interessiert bist, bei der Entwicklung von tel mitzuhelfen, gibt es viele Möglichkeiten, auf denen sowohl erfahrene als auch unerfahrene Benutzer helfen können:</p>

<ul>
<li>Du kannst einfach tel testen und uns alle noch unbekannten Fehler, die du findest, berichten</li>
<li>Wenn du ein erfahrener Benutzer bist, kannst du anderen auf der <a href="http://lists.berlios.de/mailman/listinfo/tel-users">tel Nutzer Mailingliste</a> helfen</li>
<li>Du könntest beim Schreiben einer Dokumentation helfen</li>
<li>Du könntest tel auf anderen Betriebsystemen testen (Microsoft Windows, Apple MacOS, ..). Die Entwickler arbeiten hauptsächlich mit Linux, weswegen Tester hier immer willkommen sind</li>
<li>Du könntest in unsere Arbeit reinschauen, indem du die <a href="http://lists.berlios.de/mailman/listinfo/tel-dev">Entwickler-Mailingliste</a> liest oder <a href="index.php?to=download">die aktuellste Revision von tel runterlädst</a> und dort ein bisschen reinschnupperst</li>
<li>Helfe tel, indem du es (international) bekannt machst und anderen vorführst</li>
<li>Du kannst dir die ausstehenden Aufgaben im <a href="http://developer.berlios.de/feature/?group_id=8132">tel Feature Manager</a> ansehen und dabei helfen, diese zu implementieren</li>
</ul>

<p>
Es gibt viele Arten zu helfen und nur wenige erfordern gute Programmierkenntnisse. Wenn du Fragen oder Probleme hast oder am Projekt teilnehmen willst, <a href="index.php?to=contact">kontaktiere uns</a> ungeniert oder schreibe auf unserer <a href="http://lists.berlios.de/mailman/listinfo/tel-users">Mailingliste</a>.
</p>
