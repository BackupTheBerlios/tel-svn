<h1>Neuigkeiten</h1>
<p>Die neue Version 0.1.7 ist fertig.</p>

<p>Diese Version ist die letzte vor der Version 0.2.0,
die zur Zeit entwickelt wird. Sie enthält drei neue Felder
für die Postleitzahl, das Land und den Titel sowie einen
Löschbefehl für das Installationsscript.</p>

<p>Es wurden auch viele Fehler behoben. Die wichtigste Fehlerbehebung
beinhaltet allerdings die Kodierung der Ausgabe und dem Telefonbuch.
Ausserdem werden nun die Dateien im richtigen Pfad gesucht, das heisst,
du bekommst immer eine korrekte Ausgabe, auch wenn tel nicht in /usr/
installiert ist.
Die Meldungen und Formatierungen wurden auch ein bisschen verändert.</p>
